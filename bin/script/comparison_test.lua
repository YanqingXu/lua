-- 比较操作测试

print("=== Comparison Test ===")

local a = 10
local b = 3

print("a =", a)
print("b =", b)

print("Testing comparisons:")
if a > b then
    print("a > b: true")
else
    print("a > b: false")
end

if a < b then
    print("a < b: true")
else
    print("a < b: false")
end

if a == b then
    print("a == b: true")
else
    print("a == b: false")
end

print("=== Test End ===")
